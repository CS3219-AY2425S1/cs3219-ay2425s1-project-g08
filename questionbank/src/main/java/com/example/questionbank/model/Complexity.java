package com.example.questionbank.model;

/**
 * Enum representing the complexity levels of a question.
 * <p>
 * This enum is used to specify the difficulty level of a question.
 * It provides three possible levels:
 * <ul>
 * <li>{@code EASY} - Represents a question with an easy difficulty level.</li>
 * <li>{@code MEDIUM} - Represents a question with a medium difficulty
 * level.</li>
 * <li>{@code HARD} - Represents a question with a hard difficulty level.</li>
 * </ul>
 * </p>
 */
public enum Complexity {
    /**
     * Represents a question with an easy difficulty level.
     */
    EASY,

    /**
     * Represents a question with a medium difficulty level.
     */
    MEDIUM,

    /**
     * Represents a question with a hard difficulty level.
     */
    HARD
}
